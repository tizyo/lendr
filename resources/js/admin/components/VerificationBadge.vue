  </span>
</template>

<script setup>
import { computed } from 'vue'

const props = defineProps({
  /** 'gold' | 'blue' | 'yellow' | 'grey' | null */
  tier: {
    type: String,
    default: null,
  },
  /** 'sm' | 'md' | 'lg' */
  size: {
